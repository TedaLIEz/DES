template<size_t bits> inline
std::bitset<bits> subbitset(std::bitset<bits> set, int min, int max) {
  const int ignore_hi = bits - max;
  std::bitset<bits> range = (~std::bitset<bits>() << ignore_hi) >> ignore_hi;
  set &= range;
  return set >> min;
}
